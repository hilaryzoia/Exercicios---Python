#Text analyzer

name = str(input('Enter your full name: '))
print('Analyzing your name...')
print('Your name in capital letters is {}'.format(name.upper()))
print('Your name in lowercase is {}'.format(name.lower()))
print('Your name has a total of {} letters'.format(len(name)-name.count(' ')))
separate = name.split()
print('His first name is {} and he has {} letters'.format(separa[0],len(separa[0])))
